export const createOrUpdateVendor = (vendorData: Partial<Vendor>): Vendor => {
  const existingVendorIndex = mockVendors.findIndex(v => v.id === vendorData.id);
  
  if (existingVendorIndex !== -1) {
    // Update existing
    mockVendors[existingVendorIndex] = {
      ...mockVendors[existingVendorIndex],
      ...vendorData,
      updated_at: new Date()
    };
    return mockVendors[existingVendorIndex];
  } else {
    // Create new
    const newVendor: Vendor = {
      id: Date.now().toString(),
      name: vendorData.name || '',
      legal_name: vendorData.legal_name || '',
      tax_id: vendorData.tax_id || '',
      vendor_type: vendorData.vendor_type || 'Professional Services',
      vendor_category: vendorData.vendor_category || 'Standard Vendor',
      status: vendorData.status || 'Active',
      overall_risk_level: vendorData.overall_risk_level || 'Medium',
      primary_contact_name: vendorData.primary_contact_name || '',
      primary_contact_email: vendorData.primary_contact_email || '',
      address: vendorData.address || {
        street: '',
        city: '',
        state: '',
        postal_code: '',
        country: 'Brasil'
      },
      relationship_manager: vendorData.relationship_manager || '',
      onboarding_date: vendorData.onboarding_date || new Date(),
      business_criticality: vendorData.business_criticality || 'Medium',
      data_access_level: vendorData.data_access_level || 'Internal',
      service_level_requirements: vendorData.service_level_requirements || [],
      certifications: vendorData.certifications || [],
      compliance_requirements: vendorData.compliance_requirements || [],
      regulatory_requirements: vendorData.regulatory_requirements || [],
      monitoring_frequency: vendorData.monitoring_frequency || 'Quarterly',
      auto_renewal: vendorData.auto_renewal || false,
      created_by: vendorData.created_by || 'system',
      created_at: new Date(),
      updated_by: vendorData.updated_by || 'system',
      updated_at: new Date(),
      version: '1.0',
      ...vendorData
    };
    
    mockVendors.push(newVendor);
    return newVendor;
  }
};